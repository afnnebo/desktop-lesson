example = 'Andrei'
print(example[:1]) # Вывед на экран(в консоль) первый символ этой строки.
print(example[-1]) # Вывел на экран(в консоль) последний символ этой строки (используя отрицательный индекс).
print(example[3:]) # Вывел на экран(в консоль) вторую половину этой строки (С нечётным количеством символов).
print(example[::-1]) # Вывел на экран(в консоль) это слово наоборот.
print(example[1:6:2]) # Вывел на экран(в консоль) каждый второй символ этой строки.